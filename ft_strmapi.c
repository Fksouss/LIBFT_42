#include"libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	char	*str;

	i = -1;
	str = ft_strdup(s);
	while (str[++i])
		str[i] = (f)(i, str[i]);
	return (str);
}
