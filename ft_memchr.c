#include"libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	int	i;

	i = -1;
	while (++i < (int)n - 1)
		if (((char *)s)[i] == (unsigned char)c)
			return ((char *)s + i);
	return (NULL);
}
